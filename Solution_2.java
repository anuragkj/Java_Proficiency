import java.util.*;

public class Solution_2 {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        String a = scan.next();
        int a1 = Integer.parseInt(a);
        
        String b = scan.next();
        int b1 = Integer.parseInt(b);
        
        String c = scan.next();
        int c1 = Integer.parseInt(c);
        
        System.out.print(a1 + "\n" + b1 + "\n" +c1);
        
    }
}